package com.thevisitcompany.gae.deprecated.model.users.account.utility;

/**
 * 
 * @author dereekb
 * 
 */
public enum AccountPermission {

	/**
	 * Can add items to the account.
	 */
	ADD_ITEM,

	/**
	 * Can view items in the account.
	 */
	VIEW_ITEM,

	/**
	 * Can remove items from the account.
	 */
	REMOVE_ITEM,

	/**
	 * Can view account information.
	 */
	VIEW_ACCOUNT,

	/**
	 * Change change account information.
	 */
	UPDATE_ACCOUNT,

	/**
	 * Can add members to the account.
	 */
	ADD_MEMBER,

	/**
	 * Can view the members list.
	 */
	VIEW_MEMBERS,

	/**
	 * Can remove members from the account.
	 */
	REMOVE_MEMBER

}
